# frozen_string_literal: true

# =============================================================================
# 00_registries.rb — プラグイン共通レジストリ
# =============================================================================
#
# 他のプラグインファイルが利用するレジストリを定義する。
# ファイル名の "00_" プレフィックスにより、_plugins/ 内の他のすべてのファイルよりも
# 先に読み込まれることが保証される。
#
# =============================================================================

# =============================================================================
# Say モジュール — 吹き出し（Speech Bubble）プラグインの名前空間
# =============================================================================
#
# 吹き出しプラグインのレジストリ、色ユーティリティ、および共通機能を提供する。
# Liquid タグの実装は say.rb に、各キャラクターの Provider は個別のファイルに分離されている。
#
module Say

  # ===========================================================================
  # キャラクターレジストリ
  # ===========================================================================

  # 登録済みキャラクターのハッシュ。
  # キャラ名（String）をキー、Provider インスタンスを値とする。
  # 直接操作せず、register_character メソッドを通じて登録すること。
  CHARACTERS = {}

  # キャラクターを登録する。
  # 各 Provider ファイルのトップレベルから呼び出すことを想定している。
  #
  # 使用例:
  #   Say.register_character("empty", Say::EmptyProvider.new)
  #
  def self.register_character(name, provider)
    CHARACTERS[name] = provider
  end

  # ===========================================================================
  # HTML ユーティリティ
  # ===========================================================================

  # .say__face div を組み立てるヘルパー。
  # プロバイダーの resolve はこれを使って顔部分の HTML を返すことを推奨する。
  # inner を省略すると空の div となり、顔アイコンを持たない吹き出しの
  # プレースホルダーとして機能する（flex レイアウトの幅を確保するため）。
  def self.face_html(inner = "")
    %(<div class="say__face">#{inner}</div>)
  end

  # ===========================================================================
  # 色ユーティリティ
  # ===========================================================================

  # 16進数カラーコード文字列を [R, G, B] の整数配列にパースする。
  # "#rgb" の3桁短縮形と "#rrggbb" の6桁形式に対応する。
  # "#" プレフィックスは省略可能。
  # パースに失敗した場合は nil を返す。
  def self.parse_hex_color(hex)
    hex = hex.delete_prefix("#")
    case hex.length
    when 3
      # 短縮形: 各桁を2回繰り返して6桁に展開する（例: "f0a" → "ff00aa"）
      hex.chars.map { |c| (c * 2).to_i(16) }
    when 6
      # 完全形: 2桁ずつ区切って変換する
      hex.scan(/../).map { |c| c.to_i(16) }
    else
      nil
    end
  end

  # R, G, B の整数値（各 0〜255）を "#rrggbb" 形式の文字列に変換する。
  # 範囲外の値は 0〜255 にクランプされる。
  def self.rgb_to_hex(r, g, b)
    "#%02x%02x%02x" % [r.clamp(0, 255), g.clamp(0, 255), b.clamp(0, 255)]
  end

  # R, G, B の整数値から、W3C の定義に基づく相対輝度（0.0〜1.0）を計算する。
  # sRGB のガンマ補正を考慮した線形化を行った上で、輝度係数で重み付けする。
  def self.relative_luminance(r, g, b)
    [r, g, b]
      .map { |c| c / 255.0 }
      .map { |c| c <= 0.03928 ? c / 12.92 : ((c + 0.055) / 1.055)**2.4 }
      .then { |rs, gs, bs| 0.2126 * rs + 0.7152 * gs + 0.0722 * bs }
  end

  # 塗りつぶし色から、視認性の良い枠線色を自動導出する。
  # 明るい色（相対輝度 > 0.5）には暗い枠線を、暗い色には明るい枠線を生成する。
  # パースに失敗した場合は中間グレー "#888888" を返す。
  def self.derive_border_color(fill_hex)
    rgb = parse_hex_color(fill_hex)
    return "#888888" unless rgb

    lum = relative_luminance(*rgb)
    if lum > 0.5
      # 明るい塗り → 各チャンネルを60%に暗くする
      rgb_to_hex(*rgb.map { |c| (c * 0.6).round })
    else
      # 暗い塗り → 各チャンネルを白方向に40%明るくする
      rgb_to_hex(*rgb.map { |c| (c + (255 - c) * 0.4).round })
    end
  end
end
