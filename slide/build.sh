#!/usr/bin/env bash
# ============================================================
#   スフィアクラフト紹介スライドのビルドツールなのだぁ🌱
#   このスクリプトの場所へ cd してから、同じディレクトリの build.xa1（xarpite / API5）を呼ぶだけなのだぁ〜
#   ビルドの中身（素材の配置・HTML/CSS の生成・完了メッセージ）は、ぜんぶ xarpite 側でやるのだぁ🌱
#   xarpite ランタイムはリポジトリ同梱の ../xarpite を相対パスで使うので、PATH 指定は不要なのだぁ🌱
# ============================================================
set -euo pipefail

# このスクリプトの場所をプロジェクトルートとして扱うのだぁ🌱
# 先頭が - のパスでも壊れないように、cd と dirname の両方に -- を付けるのだぁ〜
cd -- "$(dirname -- "$0")"

# リポジトリ同梱の xarpite（相対パス）で build.xa1 を実行するのだぁ🌱 追加の引数は ARGS としてそのまま渡すのだぁ〜
../xarpite/xarpite -A 5 -q -f build.xa1 "$@"
